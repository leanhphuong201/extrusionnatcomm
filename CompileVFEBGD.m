%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% The function below is to plot the kymograph for traction force of individual 
% data with respected to the center of extrusion. x axis is the time and y-axis is
% the radius from the extrusion center. y axis is the average force for each ring
% of width = Widths starting of radius between 
% reMin < r < reMax from extrusion center. 
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%Change parameters here

      %for the range of Vflux kymograph
       reMin=0;%20;  %pix
       reMax=200; %pix
       Nre=21;%43; 
       fNbefaft=100; %frame before and after extrusion
        Widths=(reMax-reMin)/(Nre-1);  %s.t. =5

      
AllAvgVFlux_test=GetAllAvgVFlux(reMin,reMax,Nre,Widths,fNbefaft,ExtList,x_TFM,y_TFM,u_TFM,v_TFM);


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%pull all exps into one averaged graph of phase  1 and phase 2
        %phase1

 
  %%%%%%%%%%%%%%
 %plot x-y inverted kymograph, and also in hours for paper %%
 figure
    
    h= surf([-100:100]*3,[reMin:(reMax-reMin)/(Nre-1):reMax]*0.1833,(AllAvgVFlux_test{2,1})'*1); ylabel('radius (\mum)'); xlabel('min'); 
    colorbar
    az = 0;    %view from overhead
    el = 90;
    view(az, el);
    set(h, 'edgecolor','none');  %no black grid lines
    title(['Radial Traction Force'])
    
    
